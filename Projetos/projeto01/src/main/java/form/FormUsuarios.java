    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //antes de salvar, eu preciso verificar se todos os campos estão preenchidos
        
        //verifica se o campo de ID está vazio
        if(txtIDUsuario.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Insira um ID númerico!");
            txtIDUsuario.requestFocusInWindow();
            return;
        }
        
        //verifica se o campo da comboBox está vazio
        if(comboBox.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(rootPane, "Selecione um tipo de perfil!");
            comboBox.requestFocusInWindow();
            return;
        }
        
        //verifica se o campo de nome está vazio
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Insira um nome!");
            txtNome.requestFocusInWindow();
            return;
        }
        
        //verifica se o campo de sobrenome está vazio
        if(txtSobrenome.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Insira um sobrenome!");
            txtSobrenome.requestFocusInWindow();
            return;
        }


        //ao clicar no botão 'salvar' todos os botões ficarão visíveis, exceto o de 'salvar' e o de 'cancelar'
        btnPrimeiro.setEnabled(true);
        btnProximo.setEnabled(true);
        btnAnterior.setEnabled(true);
        btnUltimo.setEnabled(true);
        btnNovo.setEnabled(true);
        btnExcluir.setEnabled(true);
        btnSalvar.setEnabled(false);
        btnEditar.setEnabled(true);
        btnCancelar.setEnabled(false);
        btnPesquisar.setEnabled(true);
        
        //deixando os campos de texto desabilitados 
        txtIDUsuario.setEnabled(false);
        txtNome.setEnabled(false);
        txtSobrenome.setEnabled(false);
        txtSenha.setEnabled(false);
        txtConfirmarSenha.setEnabled(false);
        comboBox.setEnabled(false);
    }//GEN-LAST:event_btnSalvarActionPerformed

