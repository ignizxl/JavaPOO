    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        //ao clicar no botão 'novo' apenas os botões 'cancelar' e 'salvar' ficarão visíveis 
        btnPrimeiro.setEnabled(false);
        btnProximo.setEnabled(false);
        btnAnterior.setEnabled(false);
        btnUltimo.setEnabled(false);
        btnNovo.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnSalvar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnCancelar.setEnabled(true);
        btnPesquisar.setEnabled(false);
        
        //deixando os campos de texto habilitados 
        txtIDUsuario.setEnabled(true);
        txtNome.setEnabled(true);
        txtSobrenome.setEnabled(true);
        txtSenha.setEnabled(true);
        txtConfirmarSenha.setEnabled(true);
        comboBox.setEnabled(true);
         
        //deixando os campos de texto 'limpo'
        txtIDUsuario.setText("");
        txtNome.setText("");
        txtSobrenome.setText("");
        txtSenha.setText("");
        txtConfirmarSenha.setText("");
        //deixando a primeira opção da combo box selecionada 
        comboBox.setSelectedIndex(0);
        
        // quando eu cadastrar um usuário novo, o atributo 'novo' que antes era falso, vai passar a ser verdadeiro 
        this.novo = true;
        
        //usando o request para jogar o cursor para o campo de texto do ID do Usuário
        txtIDUsuario.requestFocus();
        
    }//GEN-LAST:event_btnNovoActionPerformed

